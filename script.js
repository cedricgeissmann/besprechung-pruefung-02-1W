function right() {
    const main = document.querySelector("#main")
    main.className = "right"
}

function left() {
    const main = document.querySelector("#main")
    main.className = "left"
}

function up() {
    const main = document.querySelector("#main")
    main.className = "top"
}

function bottom() {
    const main = document.querySelector("#main")
    main.className = "bottom"
}

function reset() {
    const main = document.querySelector("#main")
    main.className = ""
}